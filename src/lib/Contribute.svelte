<p>
  All of the data for this site is curated by hand from the each organization's
  website (or Facebook page, or whatever they have). You may have noticed that
  some of these sites are a challenge to navigate, or worse, lead over to third
  party registration sites. This means a constant effort to keep data on
  Sherwood Sports up-to-date, and I welcome your help!
</p>

<p>
  This site is a volunteer effort by me (Jim), and you can reach a out a few
  ways with questions or contributions:
</p>
<ul>
  <li>
    Email to: <a href="mailto:jim@sherwoodsports.org">jim@sherwoodsports.org</a>
  </li>
  <li>Google Feedback form (optionally anonymous)</li>
  <li>GitHub, for the more technically inclined.</li>
</ul>

<h3>Ways to contribute</h3>
<p>
  <strong>Discovery.</strong>
  Have I missed some organzations, or have new ones opened up? Please let me know
  about them!
</p>

<p>
  <strong>Data freshness</strong>. Please report any errors, broken links, and
  help fill in missing information for the listed programs. It is also really
  helpful to hear that the information is correct! The table at the bottom of
  this page lists when an organization was last reviewed. Consider verifying one
  of the sites that hasn't been reviewed in a while and letting me know if it is
  still accurate.
</p>

<p>
  <strong>General Feedback </strong>. Are there bugs with the site itself, or
  way it could work better? It is completely custom so let me know what ideas
  you have for how we can improve it.
</p>

